public class RealtimeItem {
    private String lineid;
    private String car;
    private String time;
    private String location;

    public void setLineid() {
        this.lineid = lineid;
    }

    public String getLineid() {
        return lineid;
    }

    public void setCarNO() {
        this.car = car;
    }

    public String getCarNO() {
        return car;
    }

    public void setCarTime() {
        this.time = time;
    }

    public String getCarTime() {
        return time;
    }

    public void setCarLocation() {
        this.location = location;
    }

    public String getCarLocation() {
        return location;
    }
}
